package com.test;

public class SwapTest {

	public static void main(String[] args) {
		Integer x = 5;
		Integer y = 10;
		System.out.println(x+","+y);
		int temp = x;
		x=y;
		y=temp;
		System.out.println(x+","+y);
		System.out.println(2%3);
		System.out.println("SWAPPING COMPLETED!");
	}
}
